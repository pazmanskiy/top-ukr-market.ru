		var clock;
		
		$(document).ready(function() {
			var clock;

			clock = $('.clock').FlipClock({
		        clockFace: 'HourlyCounter',
		        autoStart: false,
				countdown: true,				
		        callbacks: {
		        	stop: function() {
		        		$('.message').html('Акция завершена!')
		        	}
		        }
		    });
				    
		    clock.setTime(12*3600);
		    clock.setCountdown(true);
		    clock.start();

		});
		
		
