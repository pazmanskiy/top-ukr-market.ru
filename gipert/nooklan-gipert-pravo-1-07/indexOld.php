<p><u><u><b>Борис Тодуров</b></u></u>: У вас не просто частный бизнес. От него зависят жизни людей. Ведь именно они покупают у вас лекарства. Вы же продаете заведомо неэффективные препараты, отказываясь от продажи всего, что может нанести ущерб вашей прибыли. Я лично присутствовал на переговорах и слышал, как ваши сотрудники говорили, что наш "Нооклан" обрушит продажи всех лекарств от сердца и гипертонии, на которые приходится две трети вашей прибыли. Это мерзко и ужасно - делать деньги на жизнях людей!</p>
	
	
<p><b><i>Наталья Мосейчук:</i></b> <b>Сергей, можете как-то прокомментировать это?</b></p>

<p><u><u><b>Сергей Давидов</b></u></u>: Не уверен, что стоит отвечать. Могу только сказать, что все продающиеся у нас препарат прошли сертификацию в Украине. Если какие-то из них запретят, то мы их сразу же снимем с продажи. Но все делается строго в соответствии с законодательством. Что касается "Нооклана" от Института Сердца, то как я уже сказал, у нас частный бизнес. И нас нельзя принудить продавать что-то против нашей воли. </p>

	
