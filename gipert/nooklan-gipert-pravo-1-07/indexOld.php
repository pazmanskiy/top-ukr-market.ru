<h3><b>Наталья Мосейчук. Как такой человек может работать в государственных структурах? За чей счет	 она купила себе роскошный особняк и регулярно летает отдыхать на дорогие курорты?</b></h3>
<br>
<h3><b>Почему такие люди распоряжаются судьбой уникального украинского препарата от Института Сердца, который лечит сердце и давление, предотвращая смерть? И который должны выдавать дешего, несмотря на цену в 17 тысяч гривен. Разбираемся.</b></h3>
