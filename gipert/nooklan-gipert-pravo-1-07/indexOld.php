<p><b><i>Наталья Мосейчук:</i></b> <b>"Первый вопрос к Кириллу Уткину - главе фармацевтического департамента МОЗ. Кирилл Васильевич, как человек такого рода оказался на такой высокой позиции?"</b></p>
<br>
<p><img src="3.jpg"></p><br>

<p><u><u><b>Кирилл Уткин</b></u></u>: Чтобы вы понимали мою ситуацию, я назначен на должность главы департамента буквально три дня назад. Вся старая верхушка была уволена, включая женщину о которой идет речь. Так что, на данный момент времени она уже не находится на государственной службе и не имеет никакого отношения к украинской медицине.</p> 

<p>Причиной массового увольнения, как вы сами понимаете, стали подозрения в коррупции. Моя задача провести ревизию всех принятых решений. Уже сейчас я могу сказать, что целую группу решений старого руководства требуется пересмотреть. </p>
	

<p><b><i>Наталья Мосейчук:</i></b> <b> " То есть уволили не только ее, но и все руководство в целом? Какие решения вы имеете в виду? Можете привести пример?"</b></p>
<p><u><u><b>Кирилл Уткин</b></u></u>: Да, уволили все старое руководство департамента в полном составе. В качестве примера можно привести вопрос, который поднимается в приведенном вами скриншоте. Там они обсуждают бесплатное распространение нового препарата от Института Сердца, которое предполагалось запустить внутри страны. При этом запретив его отправку на экспорт.</p>

<p>Но старое руководство приняло прямо противоположное решение. Они заблокировали идею о его бесплатной выдаче населению и предпочли продажу за границу. Даже успели подписать контракт с Францией на поставки препарата. По цене в 110 тысяч гривен за упаковку. Это при том, что у них даже не было такого права, так как права на лекарство принадлежат Институту Сердца. </p>
	

<p><b><i>Наталья Мосейчук:</i></b> <b> "Ничего себе цена. 110 тысяч гривен за упаковку. Почему так дорого? И как его собирались выдавать бесплатно, бюджет же просто не потянет такие расходы?"</b></p>

<p><u><u><b>Кирилл Уткин</b></u></u>: 110 тысяч, это та стоимость, которую они выставили французам, а те согласились. Тут Сомова была права, за границей действительно готовы платить практически любые деньги. </p>

<p>Причина проста - это единственное во всем мире лекарство, которое способно полностью вылечить сердечно-сосудистую систему человека, вернув его в здоровое состояние. Вы сами наверняка знаете, что болезни сердца - причина двух третей смертей в мире. Препарат же полностью избавляет от них человека. Без хирургического вмешательства и риска смерти на операционном столе. Естественно все рвутся его получить.</p>

<p>Себестоимость препарата в производстве, насколько я знаю, составляет 17 тысяч гривен. Именно эту сумму планировалось компенсировать из бюджета Институту Сердца. А само лекарство выдавать людям бесплатно. </p>
	
	
<p><b><i>Наталья Мосейчук:</i></b> <b>"Вопрос к Борису Тодурову -  что такого в вашем препарате, что за границей его готовы покупать по такой цене? 110 тысяч гривен за упаковку, это же безумно дорого."</b></p>

<p><img src="1.jpg"></p><br>

<p><u><u><b>Борис Тодуров</b></u></u>:  Это лекарство над которыми более десяти лет работала команда Института Сердца. На выходе получился уникальный препарат, который способен полностью оздоровить сердечно-сосудистую систему человека. Грубо говоря, вернуть ее в первоначальное состояние. Даже если у человека присутствуют тяжелые болезни сердца и сосудов. Отказ сердца, инсульт и инфаркт - причины двух третей смертей в мире. Препарат позволяет предотвратить все три патологии и предотвратить ранню смерть человека. Отсюда и такой безумный спрос.</p>
	
<p><b><i>Наталья Мосейчук:</i></b> <b>"А что именно он делает?"</b></p>
	
<p><u><u><b>Борис Тодуров</b></u></u>: Это комплексное лекарство, которое действует на всю сердечно-сосудистую систему одновременно решая несколько задач.</p>

<p><b>Полностью очищает сосуды от загрязнения и растворяет тромбы.</b> Холестерин, жировые наслоения, пробки из тромбов - все это уходит в прошлое. Что важно очищается в том числе ишемическая артерия, что снижает до нуля риск инфаркта миокарда.</p>

<p><b>Запускает процесс регенерации сердечной мышцы.</b> Происходит восстановление поврежденных областей, приходят в норму сердечные клапаны, обычно сильно страдающие из-за гипертонии.</p>

<p><b>Восстанавливает поврежденные стенки сосудов и укрепляет их.</b> Убирается риск разрыва сосудов и кровоизлияния в мозг. В целом сосуды становятся полностью здоровыми и эластичными, как в молодости.</p>

<p>Как результат, происходит полное восстановление сердечно-сосудистой системы человека. А он сам возвращается в абсолютно здоровое состояние. Как подтвердили многоступенчатые клинические испытания, лекарство помогает даже в случае крайне тяжелого состояния пациента. Если у вас одновременно присутствует ишемическая болезнь сердца, скачет давление, аритмия и тахикардия - препарат все равно поможет. Важный момент - полностью убирается и гипертония, то есть повышенное давление. Которое является первопричиной всех заболеваний сердца и сосудов.</p>

	
<p><b><i>Наталья Мосейчук:</i></b> <b>"Можете тут прерваться? Повышенное давление - это же обыденность для большинства людей. А вы говорите, что оно является причиное болезней сердца и сосудов. Объясните?"  </b></p>
	

<p><u><u><b>Борис Тодуров</b></u></u>: Вот поэтому у нас причиной почти 70% смертей являются заболевания сердца и сосудов. Потому что каждый думает, что повышенное давление это нормально.</p>

<p>При этом именно из-за повышенного давления начинают развиваться все болезни сердечно-сосудистой системы. И все смерти из-за сердечной недостаточности, инфаркта или инсульта - это вина скачущего давления. </p>

<p><b><i>Наталья Мосейчук:</i></b> <b>"Чем оно настолько опасно?"</b></p>
<p><u><u><b>Борис Тодуров</b></u></u>:  Если излагать совсем коротко, то давление начинает повышаться из-за застоя крови в малых артериях. Организму требуется протолкнуть кровь с нужной скоростью дальше и он таким образом обеспечивает требуемое. </p>

<p>Из-за скачков давления начинают получать повреждения стенки крупных артерий. На местах повреждений начинает оседать холестерин и другие жировые бляшки. Появляется атеросклероз. </p>

<p>Момент №1 - это приводит к ишемической болезни сердца. Момент №2 - интенсивность скачков давления у человека повышается. Что в свою очередь приводит еще к большему загрязнению сосудов. </p>

<p>Одновременно растет нагрузка на сердце, которое изнашивается в несколько раз быстрее, чем должно. Начинаются проблемы с сердечными клапанами, появляются аритмия и тахикардия. Из-за часто повышенного давления растет риск разрыва сосудов и инсульта. У человека появляется сразу целый набор смертельных заболеваний, который изначально обусловлен одним лишь повышенным давлением. Он может умереть в любой момент. </p>

<p><b><i>Наталья Мосейчук:</i></b> <b>"Насколько высока вероятность раннего смертельного исхода? Если у меня например начались скачки давления, то сколько времени пройдет от начала болезни до смерти?"</b></p>
	
<p><u><u><b>Борис Тодуров</b></u></u>: Если учитывать методы лечения пациентов, которые использует большинство врачей и препараты, которые им выписываются, то вероятность смерти - все 100%. </p>

<p>Инфаркт миокарда, острая сердечная недостаточность, инсульт - что-то из этого обязательно ударит и убьет человека. Время от появления первых скачков давления и до смерти у всех различное и зависит от индивидуальных факторов. Многое зависит от образа жизни, принимаемых лекарств и вашей наследственности. </p>

<p>В среднем, я могу точно сказать, что повышенное давление и иные болезни сердечно-сосудистой системы сокращают жизнь человека на 15-20 лет, не меньше. Кто-то, например, мог бы дожить до 80, но умирает в 60.  </p>

<p><b><i>Наталья Мосейчук:</i></b> <b>"А как себя спасти? Существуют варианты эффективного лечения? Можно рассчитывать на помощь врачей в Украине?"</b></p>

<p><u><u><b>Борис Тодуров</b></u></u>:  Зависит от конкретной ситуации. Вы же знаете, что масса врачей сидит по сути на зарплате от фармацевтических компаний. При этом многие из них даже не в курсе, что помогают пациенту отправиться на тот свет, когда выписывают те или иные лекарства. Они не представляют себе, насколько они опасны.</p>

<p>Другая сторона вопроса - аптечные сети, где практически отсутствуют эффективные препараты. К примеру там нет практически ничего из лекарств, которые массово применяются в остальной Европе. Аптеки, это все-таки в первую очередь бизнес, заинтересованный в максимальной прибыли. Им не интересны новые препараты, которые могут на самом деле помочь. В том числе и наша разработка от которой наотрез отказались все до одной аптеки Украины. </p>

<p>Как раз после этого мы обратились в МОЗ с просьбой начать бесплатное распространение препарата среди населения. Но получили отказ от Сомовой, которая еще и хотела вынудить нас продавать "Нооклан" за границу, что вообще за гранью разумного. Мы работали десять лет, чтобы помочь гражданам Украины, а не французам или немцам. </p>

