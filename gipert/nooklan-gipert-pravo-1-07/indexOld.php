<h3><b>Несколько дней назад в социальных сетях широко разошёлся скриншот скандального сообщения Кристины Сомовой, заместителя главного фармацевта Украины. После огласки, она удалила свой аккаунт в Фейсбуке. Но у нас остался скриншот ее записи, который мы демонстрируем.  </b></h3>
