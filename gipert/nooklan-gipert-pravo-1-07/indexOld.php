<h4>Вчера стало известно, что Кристина Сомова и все руководство департамента фармацевтики уволены и там производится проверка. Чтобы понять суть происходящего, мы пригласили в студию главу фармацевтического департамента МОЗ, представителя аптечной сети "Фармсток" и руководителя Института Сердца Украины  </h4><br>
	
