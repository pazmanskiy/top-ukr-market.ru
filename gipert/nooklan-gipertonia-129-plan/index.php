                                        <p>Однако мало кто задумывается о том, что из этой проблемы со здоровьем есть и другой выход – ЧТОБЫ СНИЗИТЬ ДАВЛЕНИЕ И ПРИ ЭТОМ НОРМАЛИЗОВАТЬ КРОВООБРАЩЕНИЕ ВО ВСЕХ ВНУТРЕННИХ ОРГАНОВ, НЕОБХОДИМО ПРОЧИСТИТЬ СОСУДЫ. В этом и состоит весь секрет. Как только я начал чистить сосуды, сердечно-сосудистая система полностью оздоровилась. Сердце стало работать как часы, а давление нормализовалось. Кроме того, оздоровились все внутренние органы. </p>

                                        <p>Почки перестали болеть, суставы ныли раньше, тоже перестали, головные боли мучили, прошли. Чувствую в себе бодрость, полон сил. Как прочистил первый раз сосуды, стал заниматься спортом. Сначала бегать, потом стал плавать в бассейне. А теперь вот завоевал 2 медали. Если бы мне в 50 лет сказали, что я в 92 года стану чемпионом мира по плаванью, не поверил бы. А так я планирую прожить как минимум до 120 лет. </p>

                                        <p><b>Корреспондент: Как вы думаете, почему об этом способе нормализации давления и продления жизни так мало говорят врачи?</b> </p>

                                        <p>- Скажите, а кому нужны старики? Старики – это лишь обуза для всех. Их невыгодно лечить и продлевать им жизнь, потому что чем дольше мы живем, тем больше нам надо платить пенсию. Старики это балласт для любой экономики. Поэтому и никто не думает, чтобы удлинять нам жизнь. </p>

                                        <p>Кроме того, лечение болезней сегодня (особенно сердечных) – это многомиллиардный бизнес. Только посчитайте, сколько вы тратите на таблетки и врачей. А представьте, сколько таких больных. И все они несут деньги. Поэтому фармацевтам просто невыгодно, чтобы люди становились здоровыми. Намного выгоднее их пичкать малоэффективными лекарствами, которые помогают лишь временно. </p>

                                        <p>Только мы сами хотим жить дольше, а больше никто этого не хочет, поэтому только мы сами можем себя вылечить. Принимать таблетки для снижения давления – это явно не выход. Если хотите жить долго, чистите сосуды. Только так. </p>

                                        <p><b>Корреспондент: Не могли бы вы рассказать, как именно вы чистите сосуды?</b> </p>

                                        <p>- На самом деле сейчас это делать проще простого. Это раньше надо было заказывать специальные травы из Закарпатья, делать из них специальные настойки и очищать сосуды месяцами (первая очистка заняла у меня почти 6 месяцев!). Сейчас этого делать ничего не нужно. </p>

                                        <p>Есть очень хороший препарат в котором уже собраны все микроэлементы нужные для чистки сосудов, называется он Нооклан. Его специально разработали для очистки сосудов. Просто пьёте этот препарат 2 раза в день и все. </p>

                                        <p>Чистку сосудов я рекомендую проводить 1 раз в 2-3 года. Если вы будете это делать, проживете и 80 лет, и 100 лет, а может и 120 лет. При этом не будете болеть и будете активными и жизнерадостными. Поверьте, это лучше чем чувствовать себя разваливающимся и доживающим последние годы стариком! </p>

                                        <p>Способ, рассказанный украинским пенсионером-спортсменом, действительно кажется рабочим и эффективным. Неужели это так? Прежде чем публиковать этот материал и делать его доступным для читателей мы решили проконсультироваться у известного украинского кардиолога, ведущего специалиста института кардиологии имени академика М.Д. Стражеска, заслуженного профессора Украинской академии наук Александра Викторовича Недоступа </p>

