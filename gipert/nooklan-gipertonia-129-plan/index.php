                                        <p>Анатолий Васильевич Лисицкий завоевал 2 золотые медали и установил мировой рекорд в заплыве на 50 метров на проходивших в Будапеште международных соревнованиях по плаванию среди ветеранов. Анатолию Васильевичу 92 года, при этом по силе, активности, здоровью он даст фору многим 40-летним. </p>

                                        <p>Но Анатолий не всегда имел богатырское здоровье. После 50 лет он был больным человеком, в 60 пережил обширный инфаркт, а в 68 лет ему поставили инвалидность первой группы… </p>

                                        <p>Когда жить оставалось совсем немного, Анатолий Васильевич Лисицкий решил всерьез взяться за свое здоровье. В итоге уже через 2 года инвалидность сняли. А в 80 лет, врачи проводившие обследование пенсионера, удивились, насколько он здоров: «хоть завтра в космос отправляй». </p>

                                        <p>В 92 года Анатолий Васильевич Лисицкий стал чемпионом мира по плаванию. И это притом, что Анатолий Васильевич живет в Полтаве и получает мизерную пенсию, которой явно не хватает на дорогие лекарства. </p>

                                        <p>Как же мужчине удалось укрепить свое здоровье, продлить свою жизнь и даже стать чемпионом мира? </p>

                                        <p>Анатолий Васильевич поделился секретом долголетия с нашим корреспондентом </p>

