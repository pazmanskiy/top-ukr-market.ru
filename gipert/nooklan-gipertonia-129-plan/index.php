                                        <p><b>Корреспондент: Анатолий Васильевич, главный вопрос – как сохранять такую жизнерадостность, здоровье, бодрость, да еще и медали завоевывать в столь почтенном возрасте. В чем ваш секрет?</b> </p>

                                        <p>- Я не всегда был таким. Я всю жизнь проработал на стройке, сначала прорабом, а потом в строительном фонде в Полтаве. А это очень нервная и тяжелая работа. В 50 с небольшим лет у меня начало часто повышаться давление, сначала редко, потом все чаще и чаще, а потом перестало снижаться совсем. То есть, было повышенным всегда. Развилась возрастная гипертония. </p>

                                        <p>Врачи назначили лекарства для снижения давления. Сейчас уже даже не помню какие. Они помогали, но если вдруг пропустил прием такой таблетки, то давление сразу же повышалось. Насколько я знаю, даже сейчас так лечат гипертонию, за 20 лет ничего не поменялось. Может быть, только таблетки современнее стали. </p>

                                        <p>При этом я продолжал работать, хотя уже тогда чувствовал себя стариком. А однажды меня увезли на скорой прямо со строительной площадки. Был инфаркт. Фактически вернули с того света. Месяц пролежал в больнице. Потом выписали, направили к врачу. </p>

                                        <p>Тот к уже имеющимся лекарствам для снижения давления выписал другие. Как сейчас помню, каждый день принимал чуть ли ни горсть таблеток. Уходило много денег на их покупку. В аптеку как в продуктовый магазин ходил. </p>

                                        <p>Дозы таблеток приходилось повышать. Но от такого лечения болезнь не прошла. И не могла пройти. В 68 лет мне дали инвалидность первой группы и отправили на пенсию. </p>

                                        <p>Фактически меня отправили доживать мои 3-4 года, ну может быть, 5 лет. Да я и сам понимал, что жизнь моя заканчивается. Чувствовал себя неважно. Если вдруг просыпался с хорошим самочувствием, и ничего не болело, это было для меня как праздник. </p>

                                        <p>Наверное, и умер бы так. Если бы ни один мой старый знакомый. У него брат был академиком в Свердловском кардиологическом институте еще при Брежневе. Он мне сразу сказал, что это у меня сосуды грязные. И что если очищу их, то еще могу пожить. Он мне объяснил следующее, что, по сути, сосуды – это как трубы, только внутри человека. По ним ко всем органам течет кровь, которая несет питательные вещества и удаляет из органов продукты переработки. </p>

                                        <p>С возрастом сосуды загрязняются холестерином и другими веществами, так как нередко мы едим не очень здоровую и полезную пищу, пьем, курим, часто нервничаем. Сосуды загрязняются как трубы ржавчиной. Холестерин откладывается на стенках слоем, а также скапливается в различных перегибах. В итоге просвет в сосудах сужается, крови к органам начинает меньше поступать. Все органы начинают голодать и болеть. Представьте, что было бы с вами, если бы вы стали есть в 3, в 5 раз меньше, чем едите сейчас. Вы бы стали истощенными, то же самое происходит и с нашими внутренними органами. </p>

                                        <p>Чтобы как-то исправить ситуацию, сердце начинает активно качать кровь. В итоге происходит естественная реакция – АРТЕРИАЛЬНОЕ ДАВЛЕНИЕ ПОВЫШАЕТСЯ. </p>

                                        <p>Да, высокое давление, безусловно, опасно для сосудов, так как может их порвать или привести к отрыванию тромба, что может привести к инфаркту или инсульту. Поэтому врачи и выписывают лекарства для снижения давления. Однако из-за снижения давления органы опять перестают получать нужные вещества. Получается замкнутый круг. </p>

