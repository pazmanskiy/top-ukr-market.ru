        <h2>
            <b>Сегодня у нас в гостях легендарный новатор и кандидат наук из Киева — Романов Петр Геннадиевич.</b>
        </h2>


        <br>

        <p><b>— Здравствуйте, Петр Геннадиевич. Расскажете о себе?</b></p>

        <p>Родился и вырос в Киеве. Закончил Киевский государственный Медицинский Университет в 1975 г. В
            университете познакомился со своей будущей женой Ларисой. До 2009 г. работал по специальности —
            врач-ортопед.</p>

        <img src="photo1.jpeg" alt="image" class="m-img">


        <p><b>— Интересно. А что же произошло в 2009 году и почему вы бросили работу ортопеда?</b></p>

        <p>В 2007 г. у Ларисы диагностировали артроз коленного сустава. Стоит признаться, это был переломный момент в
            нашей жизни, хоть и тогда это казалось невыносимым испытанием. <b>За 2 года мы перепробовали все:
                физиотерапию, отказались от физических нагрузок, витаминные комплексы (хондроитин, глюкозамин, MSM и
                другие), гели, мази. Всё было тщетно.</b> Ничего не работало, мы были опустошены. Я не мог поверить, что
            со своим медицинским образованием я ничего не могу сделать, чтобы помочь своей жене. Тогда я окончательно
            разочаровался в нашей медицине: Неужели нет никакого способа дать счастливую и долгую жизнь своему близкому?
            И я начал исследования.</p>
        <br>

        <img src="photo2.jpeg" alt="image" class="m-img">

        <img src="photo3.jpeg" alt="image" class="m-img">


        <br>
        <p><b>— Какие исследования? Можно подробнее?</b></p>

        <p>Когда ты понимаешь, что твоя жена через несколько лет может перестать ходить, ты идешь на всё. Я начал
            исследования. Я изучал все материалы, связанные с болезнью суставов, прямо или косвенно. Тут и физиология, и
            психосоматика, и биохимия, чего я только не изучал. Я потратил почти все свои деньги, чтобы перенять опыт
            лучших специалистов из Азии, которые уж точно лучше понимали проблему больных суставов.</p>
        <p><span style="font-weight:700;">В декабре 2009 г. я смог собрать все знания, которые накопил к тому моменту, и понял, что если смешать определенные компоненты, то можно получить средство, которое навсегда избавит от болей в суставах.</span>
            Но тут меня ждал сюрприз: нужные компоненты в стране просто напросто не продавались. Я занял крупную сумму,
            и сделал заказ из Азии. Через месяц компоненты пришли, но и здесь меня ждал сюрприз — никто не хотел
            произвести нужное мне средство. Благо, выручили университетские друзья. Спустя 3 недели я получил формулу и
            мы с Ларисой попробовали. </p>
        <p><i><b>Господь Всемогущий! Моему счастью не было предела!</b></i></p>
        <p><strong>— Что? Что же произошло?</strong></p>

        <p>Лариса преображалась с каждым днем. <span style="font-weight: 700;">Спустя 7 дней лечения она легко ходила в магазин, покупала продукты и на ее лице начала появляться та самая улыбка, по которой я так скучал. Спустя еще 2 недели артроз полностью прошел!</span>
            Я не верил своим глазам, но диагностика показала, что Лариса полностью здорова. Я ликовал. Это была самая
            настоящая победа!</p>
        <br>
        <img src="photo4.jpeg" alt="image" class="m-img">

        <br>
        <p><strong>— Вы большой молодец, пример для многих в нашей стране! Но вы пошли дальше? </strong></p>

        <p>Не сразу. Первое время мы просто радовались жизни и ценили каждый момент, в этот период Лариса подарила мне
            прекрасную дочь и благодарила меня за мою разработку, ведь беременность - это тоже огромная нагрузка на
